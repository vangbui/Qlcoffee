    public void loadBangSP() {
        try {
            con = DriverManager.getConnection("jdbc:mysql://localhost:3306/qlqcf", "root", "2509");
            ps = con.prepareStatement("select * from qlqcf.chitietdathang join dathang on chitietdathang.MaDH=dathang.MaDH"
                    + " join sanpham on chitietdathang.MaSP=sanpham.MaSP Order by chitietdathang.MaDH DESC");
            rs = ps.executeQuery();
            int price, quantity, into;
            while (rs.next()) {
                row = new Vector();
                price = Integer.parseInt(rs.getString("GíaSP"));
                quantity = Integer.parseInt(rs.getString("SoLuong"));
                into = price * quantity;
                row.add(rs.getString("MaDH"));
                row.add(rs.getString("TenSP"));
                row.add(rs.getString("GíaSP"));
                row.add(rs.getString("MaLSP"));
                row.add(rs.getString("SoLuong"));
                tblModel.addRow(row);
            }
            j_thongke.setModel(tblModel);
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Lỗi 101:: Không thể kết nối đến máy chủ");
        }
    }
