    private void bt_timkiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_timkiemActionPerformed
       tblModel.getDataVector().removeAllElements();
        String s1 = (String) cb_tk.getSelectedItem();
        if (cb_tk.getSelectedIndex() == -1) {
            JOptionPane.showMessageDialog(null, "Tên sản phẩm không được để trống.");
            bt_in.setEnabled(false);
        } else {
            try {

                psTK = con.prepareStatement("select * from qlqcf.chitietdathang join dathang on chitietdathang.MaDH=dathang.MaDH"
                        + " join sanpham on chitietdathang.MaSP = sanpham.MaSP where TenSP=?");
                psTK.setString(1, (String) cb_tk.getSelectedItem());
                rsTK = psTK.executeQuery();
                if (rsTK.next()) {
                    psTK = con.prepareStatement("select * from qlqcf.chitietdathang join dathang on chitietdathang.MaDH=dathang.MaDH"
                            + " join sanpham on chitietdathang.MaSP = sanpham.MaSP where TenSP=?");
                    psTK.setString(1, (String) cb_tk.getSelectedItem());
                    rsTK = psTK.executeQuery();
                    while (rsTK.next()) {
                        vecTK = new Vector();
                        vecTK.add(rsTK.getString("MaDH"));
                        vecTK.add(rsTK.getString("TenSP"));
                        vecTK.add(rsTK.getString("GíaSP"));
                        vecTK.add(rsTK.getString("MaLSP"));
                        vecTK.add(rsTK.getString("SoLuong"));
                        tblModel.addRow(vecTK);
                    }
                    j_thongke.setModel(tblModel);
                } else {
                    JOptionPane.showMessageDialog(null, "Không tìm được sản phẩm");
                    loadBangSP();
                }
            } catch (Exception e) {
            }
            int line = j_thongke.getRowCount();
            int tong = 0, tinhtien = 0;
            for (int i = 0; i < line; i++) {
                int SL = Integer.parseInt((String) j_thongke.getValueAt(i, 4));
                int tien = Integer.parseInt((String) j_thongke.getValueAt(i, 2));
                tong += SL;
                tinhtien += tien;
//                spQuantity.setValue(quanTotal);
//            }
            }
            tf_gia.setText(String.valueOf(nft.format(tinhtien)));
            tf_soluong.setText(String.valueOf(tong));
            cb_tk.setSelectedIndex(-1);
        }
        bt_in.setEnabled(true);
    }//GEN-LAST:event_bt_timkiemActionPerformed

